export async function crawlDocument(
  url: string,

  chunkSize: number,
  overlap: number
): Promise<void> {
  const response = await fetch('/api/crawl-website', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      url,
      options: {
        chunkSize,
        overlap,
      },
    }),
  });

  const { documents } = await response.json();

  return documents;
}


